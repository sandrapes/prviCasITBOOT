package treca.nedeljaOOP.cetvrtak;

public class Modifikatori {

    /*
     * Modifikatori nam govore
     * o vidiljivosti nasih atributa i metoda
     *
     * Modofikatori:
     * 1. public - najopstiji modifikator
     * 2. / - (podrazumevani)
     * 3. protected
     * 4. private - najuzi modifikator
     *
     * 1. public modifikator
     *     - mogu sve klase iz celog PROJEKTA da vide
     *
     * 2. podrazumevani modifikator
     *    - ne mogu da ga vide klase van istog paketa
     *    - ne mogu da ga vide podklase koje su van istog paketa
     *
     * 3. protected modifikator (modifikacija public modfikatora)
     *    - ne mogu da ga vide klase koje su van istog paketa
     *
     * 4. private modifikator
     *    - mozemo ga videti samo UNUTAR ISTE KLASE
     *
     * */
}
